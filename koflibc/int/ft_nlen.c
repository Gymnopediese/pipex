int	ft_nlen(long nb, int n)
{
	int		len;

	len = 0;
	if (nb < 0)
	{
		nb = nb * -1;
		len++;
	}
	while (nb > 0)
	{
		nb = nb / n;
		len++;
	}
	return (len);
}
